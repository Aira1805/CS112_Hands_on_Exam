decimal_number = 1  # The decimal number you want to convert to binary
binary_number = bin(decimal_number)  # Convert to binary
print("Binary representation:", binary_number)


decimal_number = 2  # The decimal number you want to convert to octal
octal_number = oct(decimal_number)  # Convert to octal
print("Octal representation:", octal_number)


decimal_number = 3  # The decimal number you want to convert to hexadecimal
hexadecimal_number = hex(decimal_number)  # Convert to hexadecimal
print("Hexadecimal representation:", hexadecimal_number)